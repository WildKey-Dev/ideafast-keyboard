    /**
     * Create and show a simple notification containing the received FCM message.
     *
     * @param messageBody FCM message body received.
     */
    private void sendNotification(String messageBody, String title) {
        Intent pra = new Intent(getApplicationContext(), PromptLauncherActivity.class);
        PendingIntent pi = PendingIntent.getActivity(getApplicationContext(), 0,pra, PendingIntent.FLAG_UPDATE_CURRENT);

        // Build notification based on Intent
        Notification notification = new NotificationCompat.Builder(getApplicationContext(), CHANNEL_ID)
                .setSmallIcon(R.drawable.ic_launcher_keyboard)
                .setContentTitle(title)
                .setContentText(messageBody)
                .setPriority(NotificationCompat.PRIORITY_DEFAULT)
                // Set the intent that will fire when the user taps the notification
                .setContentIntent(pi)
                .setAutoCancel(true)
                .build();

        // Show notification
        NotificationManager manager = (NotificationManager) getApplicationContext().getSystemService(Context.NOTIFICATION_SERVICE);
        manager.notify(42, notification);

    }
